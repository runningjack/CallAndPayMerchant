    public void takePictureWP(View view) {
        if (mPhotograph != null) {
            mPhotograph.setResultListener(new Photograph.OnResultListener() {

                @Override
                public void onResult(int result, byte[] data, String err) {

                    if (data != null) {
                        mImageView.setImageBitmap(BitmapFactory.decodeByteArray(
                                data, 0, data.length));
                        mImageView.setVisibility(View.VISIBLE);

                    } else {
                        Toast.makeText(TakePictureActivity.this, "Acquire picture failed" + err,
                                Toast.LENGTH_SHORT).show();
                        mImageView.setVisibility(View.GONE);

                    }

                }
            });
            boolean isCrop = true;
            mPhotograph.takePicture(isCrop);
        } else {
            Toast.makeText(TakePictureActivity.this, "Initialize photograph failed", Toast.LENGTH_LONG)
                    .show();
        }
    }
